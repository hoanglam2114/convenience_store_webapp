package model;

/**
 *
 * @author nguye
 */
public class EmployeeWithRole {
    private int id;
    private String name;
    private String roleName;

    public EmployeeWithRole() {
    }

    public EmployeeWithRole(int id, String name, String roleName) {
        this.id = id;
        this.name = name;
        this.roleName = roleName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }
    
    
}
